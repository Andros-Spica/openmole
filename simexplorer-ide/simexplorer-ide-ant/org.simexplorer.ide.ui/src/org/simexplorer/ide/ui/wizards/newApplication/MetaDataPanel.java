        remove.addActionListener(new ActionListener() {

            public void actionPerformed(ActionEvent e) {
                if (e.getActionCommand().equals("remove")) {
                    // custon remove of component under the mouse (with the contains(Point) method it's not working espected
                    Point posDragedElt = MouseInfo.getPointerInfo().getLocation();
                    for (int i = 3; i <
                            ((MetaDataPanel)metadataPanel).getPanel().getComponentCount(); i++) {
                        Point p = ((MetaDataPanel)metadataPanel).getPanel().getComponent(i).getLocationOnScreen();
                        if ((p.x < posDragedElt.x) &&
                                (p.x +
                                ((MetaDataPanel)metadataPanel).getPanel().getComponent(i).getWidth() >
                                posDragedElt.x)) {
                            if ((p.y < posDragedElt.y) &&
                                    (p.y +
                                    ((MetaDataPanel)metadataPanel).getPanel().getComponent(i).getHeight() >
                                    posDragedElt.y)) {
                                ((MetaDataPanel)metadataPanel).getPanel().remove(i);
                                ((MetaDataPanel)metadataPanel).getPanel().revalidate();
                                ((MetaDataPanel)metadataPanel).getPanel().repaint();
                            }
                        }
                    }
                }
            }
        });
        removeMetadataPopupMenu.add(remove);
